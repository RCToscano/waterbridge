
package br.com.waterbridge.modelo;

public class Pass {
    
    private Long idPass;
    private Long idUser;
    private String pass;

    public Long getIdPass() {
        return idPass;
    }

    public void setIdPass(Long idPass) {
        this.idPass = idPass;
    }

    public Long getIdUser() {
        return idUser;
    }

    public void setIdUser(Long idUser) {
        this.idUser = idUser;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}
